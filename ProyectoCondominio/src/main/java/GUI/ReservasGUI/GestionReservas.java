    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int tipo = this.cmBoxAreaComun.getSelectedIndex();
        String dia = this.cmBoxDia.getItemAt(this.cmBoxDia.getSelectedIndex());
        boolean reserva = administrar.realizarReserva(this.txtDetalle.getText(), administrador, tipo, dia, this.txtCorreo.getText());
        if (reserva) {
            JOptionPane.showMessageDialog(null, "Reserva hecha", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            this.jTAlista.setText(administrar.toString());
        } else {
            JOptionPane.showMessageDialog(null, "Reserva no hecha", "Informacion", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

