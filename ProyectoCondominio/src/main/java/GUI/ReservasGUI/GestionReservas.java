    private void jBAcepReproActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAcepReproActionPerformed
        int id = Integer.parseInt(this.jTextFieldResrRepro.getText());
        String dia = this.cmBoxDia1.getItemAt(this.cmBoxDia1.getSelectedIndex());
        boolean reserva = administrar.reprogramarReserva(id, dia);
        if (reserva) {
            JOptionPane.showMessageDialog(null, "Reserva reprogramada", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            this.jTAlista.setText(administrar.toString());
        } else {
            JOptionPane.showMessageDialog(null, "Reserva no reprogramada", "Informacion", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBAcepReproActionPerformed

